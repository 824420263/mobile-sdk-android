package com.appnexus.opensdk.vastdata;

public class IconModel {
private String program;
private String width;
private String height;
private String xPosition;
private String yPosition;
private String duration;
private String offset;
private String apiFramework;
private ResourceModel resource;
private String iconViewTracking;

/**
 * @return the program
 */
public String getProgram() {
	return program;
}
/**
 * @param program the program to set
 */
public void setProgram(String program) {
	this.program = program;
}
/**
 * @return the width
 */
public String getWidth() {
	return width;
}
/**
 * @param width the width to set
 */
public void setWidth(String width) {
	this.width = width;
}
/**
 * @return the height
 */
public String getHeight() {
	return height;
}
/**
 * @param height the height to set
 */
public void setHeight(String height) {
	this.height = height;
}
/**
 * @return the xPosition
 */
public String getxPosition() {
	return xPosition;
}
/**
 * @param xPosition the xPosition to set
 */
public void setxPosition(String xPosition) {
	this.xPosition = xPosition;
}
/**
 * @return the yPosition
 */
public String getyPosition() {
	return yPosition;
}
/**
 * @param yPosition the yPosition to set
 */
public void setyPosition(String yPosition) {
	this.yPosition = yPosition;
}
/**
 * @return the duration
 */
public String getDuration() {
	return duration;
}
/**
 * @param duration the duration to set
 */
public void setDuration(String duration) {
	this.duration = duration;
}
/**
 * @return the offset
 */
public String getOffset() {
	return offset;
}
/**
 * @param offset the offset to set
 */
public void setOffset(String offset) {
	this.offset = offset;
}
/**
 * @return the apiFramework
 */
public String getApiFramework() {
	return apiFramework;
}
/**
 * @param apiFramework the apiFramework to set
 */
public void setApiFramework(String apiFramework) {
	this.apiFramework = apiFramework;
}
/**
 * @return the resource
 */
public ResourceModel getResource() {
	return resource;
}
/**
 * @param resource the resource to set
 */
public void setResource(ResourceModel resource) {
	this.resource = resource;
}
public String getIconViewTracking() {
	return iconViewTracking;
}
public void setIconViewTracking(String iconViewTracking) {
	this.iconViewTracking = iconViewTracking;
}



}
