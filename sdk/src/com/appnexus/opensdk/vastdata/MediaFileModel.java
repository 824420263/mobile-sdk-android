package com.appnexus.opensdk.vastdata;

public class MediaFileModel {

	private String id;
	private String delivery;
	private String type;
	private String bitrate;
	private String minBitrate;
	private String maxBitrate;
	private String width;
	private String height;
	private String scalable;
	private String maintainAspectRatio;
	private String codec;
	private String apiFramework;
	private String url;
	
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the delivery
	 */
	public String getDelivery() {
		return delivery;
	}
	/**
	 * @param delivery the delivery to set
	 */
	public void setDelivery(String delivery) {
		this.delivery = delivery;
	}
	/**
	 * @return the type
	 */
	public String getType() {
		return type;
	}
	/**
	 * @param type the type to set
	 */
	public void setType(String type) {
		this.type = type;
	}
	/**
	 * @return the bitrate
	 */
	public String getBitrate() {
		return bitrate;
	}
	/**
	 * @param bitrate the bitrate to set
	 */
	public void setBitrate(String bitrate) {
		this.bitrate = bitrate;
	}
	/**
	 * @return the minBitrate
	 */
	public String getMinBitrate() {
		return minBitrate;
	}
	/**
	 * @param minBitrate the minBitrate to set
	 */
	public void setMinBitrate(String minBitrate) {
		this.minBitrate = minBitrate;
	}
	/**
	 * @return the maxBitrate
	 */
	public String getMaxBitrate() {
		return maxBitrate;
	}
	/**
	 * @param maxBitrate the maxBitrate to set
	 */
	public void setMaxBitrate(String maxBitrate) {
		this.maxBitrate = maxBitrate;
	}
	/**
	 * @return the width
	 */
	public String getWidth() {
		return width;
	}
	/**
	 * @param width the width to set
	 */
	public void setWidth(String width) {
		this.width = width;
	}
	/**
	 * @return the height
	 */
	public String getHeight() {
		return height;
	}
	/**
	 * @param height the height to set
	 */
	public void setHeight(String height) {
		this.height = height;
	}
	/**
	 * @return the scalable
	 */
	public String getScalable() {
		return scalable;
	}
	/**
	 * @param scalable the scalable to set
	 */
	public void setScalable(String scalable) {
		this.scalable = scalable;
	}
	/**
	 * @return the maintainAspectRatio
	 */
	public String getMaintainAspectRatio() {
		return maintainAspectRatio;
	}
	/**
	 * @param maintainAspectRatio the maintainAspectRatio to set
	 */
	public void setMaintainAspectRatio(String maintainAspectRatio) {
		this.maintainAspectRatio = maintainAspectRatio;
	}
	/**
	 * @return the codec
	 */
	public String getCodec() {
		return codec;
	}
	/**
	 * @param codec the codec to set
	 */
	public void setCodec(String codec) {
		this.codec = codec;
	}
	/**
	 * @return the apiFramework
	 */
	public String getApiFramework() {
		return apiFramework;
	}
	/**
	 * @param apiFramework the apiFramework to set
	 */
	public void setApiFramework(String apiFramework) {
		this.apiFramework = apiFramework;
	}
	/**
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}
	/**
	 * @param url the url to set
	 */
	public void setUrl(String url) {
		this.url = url;
	}
}
