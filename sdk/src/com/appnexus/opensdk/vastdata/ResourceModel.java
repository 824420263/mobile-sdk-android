package com.appnexus.opensdk.vastdata;

public class ResourceModel {
	private String staticResource;
	private String iFrameResource;
	private String htmlResource;
	private String creativeType;
	/**
	 * @return the staticResource
	 */
	public String getStaticResource() {
		return staticResource;
	}
	/**
	 * @param staticResource the staticResource to set
	 */
	public void setStaticResource(String staticResource) {
		this.staticResource = staticResource;
	}
	/**
	 * @return the iFrameResource
	 */
	public String getiFrameResource() {
		return iFrameResource;
	}
	/**
	 * @param iFrameResource the iFrameResource to set
	 */
	public void setiFrameResource(String iFrameResource) {
		this.iFrameResource = iFrameResource;
	}
	/**
	 * @return the htmlResource
	 */
	public String getHtmlResource() {
		return htmlResource;
	}
	/**
	 * @param htmlResource the htmlResource to set
	 */
	public void setHtmlResource(String htmlResource) {
		this.htmlResource = htmlResource;
	}
	/**
	 * @return the creativeType
	 */
	public String getCreativeType() {
		return creativeType;
	}
	/**
	 * @param creativeType the creativeType to set
	 */
	public void setCreativeType(String creativeType) {
		this.creativeType = creativeType;
	}
}
